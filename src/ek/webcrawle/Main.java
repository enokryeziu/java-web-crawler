    private void jButton_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SubmitActionPerformed
        String[] schemes = {"http","https"};
        UrlValidator urlValidator = new UrlValidator(schemes);
        if (urlValidator.isValid(jTextField_URL.getText())) {
            swingworker = new SwingWorker<Void, Void>() {
                @Override
                protected Void doInBackground() throws Exception {
                    if(!swingworker.isCancelled()){
                        try {
                            WebCrawler wc = new WebCrawler(SelectDepth.getSelectedIndex()+2);
                            jProgressBar1.setIndeterminate(true);
                            jLabel12.setText("0");
                            jLabel13.setText("0");
                            if(jTextField1.getText().equals("Type the keyword"))
                                jTextField1.setText("");
                            wc.getPageLinks(jTextField_URL.getText(),jTextField1.getText(), 0, jTable1,jLabel12,jLabel13);
                        } catch (IOException ex) {
                            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    WebCrawler.isCancelledSW = false;
                    return null;

                }
                @Override
                protected void done() {
                    JOptionPane.showMessageDialog(null,"Ka perfunduar!");
                    jProgressBar1.setIndeterminate(false); 
                }

            };
            swingworker.execute();
         } else {
	       JOptionPane.showMessageDialog(null,"URL nuk eshte valide!");
	 }
    }//GEN-LAST:event_jButton_SubmitActionPerformed

