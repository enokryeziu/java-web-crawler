    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
                fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
                FileNameExtensionFilter filter = new FileNameExtensionFilter("xlsx", "xlsx");
                fileChooser.setFileFilter(filter);
                int returnValue = fileChooser.showOpenDialog(null);
        swingworker2 = new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                if (returnValue == JFileChooser.APPROVE_OPTION) {
                    ExportExcel ee = new ExportExcel();
                    try {
                        ee.writeXLSXFile(fileChooser.getSelectedFile().getAbsoluteFile().toString(), jTable1);
                    } catch (IOException ex) {
                        Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    System.out.println("Empty");
                }
                return null;
            }
            @Override
            protected void done() {
                JOptionPane.showMessageDialog(null,"Ka perfunduar exportimi ne Excel!");
                jProgressBar1.setIndeterminate(false); 
            }

        };
        swingworker2.execute();
    }//GEN-LAST:event_jButton2ActionPerformed
